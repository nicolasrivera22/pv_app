def tr(key: str, lang: str = "es", **kwargs) -> str:
    selected = lang if lang in TRANSLATIONS else "es"
    template = (
        TRANSLATIONS.get(selected, {}).get(key)
        or TRANSLATIONS["es"].get(key)
        or TRANSLATIONS["en"].get(key)
        or key
    )
    return template.format(**kwargs)
